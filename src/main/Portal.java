package main;

/**
 * Interfaz que define los comportamientos básicos de un portal.
 * Las clases que implementen esta interfaz deben proporcionar la funcionalidad para mostrar el portal.
 */
public interface Portal {
    /**
     * Método que se encarga de mostrar el portal.
     * Las clases que implementen esta interfaz deben proporcionar la implementación específica
     * para mostrar el contenido del portal.
     */
    void mostrarPortal();
}